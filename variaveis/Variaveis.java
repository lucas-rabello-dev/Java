public class Variaveis {
    public static void main(String[] args) {
        // <tipo> <nome> = <valor>;
        double x = 3.14159;
        System.out.println(x);
        System.out.printf("%.2f \n", x); // printa , pois depende do idioma do seu computador
        char word = 'A';
        System.out.printf("%c \n", word);
    }
}